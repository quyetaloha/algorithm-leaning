#include <iostream>
#define N 1000000007
using namespace std;

int main(){
	int t;
	cin >> t;
	while(t--){
	long long n;
	int nt = 0;
	cin >> n;
	if(n == 0) {
		cout << 0<<endl;
		return 0;
	}
	long long a[2][2] = {{0,1},{1,1}};
	long long x[2][2] = {{0,1},{1,1}};
	while(n != 0){
		long long r[2][2] ;
		r[0][0] = a[0][0];
		r[0][1] = a[0][1];
		r[1][0] = a[1][0];
		r[1][1] = a[1][1];
		long long z[2][2];
		z[0][0] = x[0][0];
		z[0][1] = x[0][1];
		z[1][0] = x[1][0];
		z[1][1] = x[1][1];
		if(n%2  == 1){
			x[0][0] = (z[0][0]*r[0][0]+z[0][1]*r[1][0])%N;
			x[0][1] = ( z[0][0] * r[0][1]+z[0][1] * r[1][1] ) % N;
			x[1][0] = (z[1][0]*r[0][0]+z[1][1]*r[1][0]) % N;
			x[1][1] = (z[1][0]*r[0][1]+z[1][1]*r[1][1]) % N;
		}
		a[0][0] = (r[0][0]*r[0][0]+r[0][1]*r[1][0]) % N;
		a[0][1] = (r[0][0]*r[0][1]+r[0][1]*r[1][1])%N;
		a[1][0] = (r[1][0]*r[0][0]+r[1][1]*r[1][0]) % N;
		a[1][1] = (r[1][0]*r[0][1]+r[1][1]*r[1][1])%N;
		n /= 2;
	}
	cout << x[0][0] <<endl;
	}
}
